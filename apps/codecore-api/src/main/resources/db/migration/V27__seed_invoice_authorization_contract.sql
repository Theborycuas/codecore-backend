-- =============================================================================
-- V27 — Invoice authorization contract (global permission seeds)
-- Schema: iam (V1)
-- PostgreSQL 16+
-- FASE 21.5 — Invoice Authorization Contract (ADR-017, ADR-007)
-- =============================================================================
-- Extends V13+V15+V19+V21+V23+V25 catalog with Billing (Invoice) lifecycle grants.
-- Idempotent via NOT EXISTS on permission.code.
-- Backfills iam.role_permission for existing system roles (idempotent NOT EXISTS).
-- No payment / tax / ledger / stock / vertical-specific permissions.
-- =============================================================================

INSERT INTO iam.permission (
    permission_id,
    code,
    description,
    system_permission,
    created_at,
    updated_at
)
SELECT
    gen_random_uuid(),
    v.code,
    v.description,
    TRUE,
    NOW(),
    NOW()
FROM (
    VALUES
        ('invoice:create', 'Create Invoice commercial claims'),
        ('invoice:read',   'Read Invoice commercial claims'),
        ('invoice:update', 'Update Invoice content while in DRAFT'),
        ('invoice:issue',  'Issue DRAFT Invoices'),
        ('invoice:void',   'Void DRAFT or ISSUED Invoices')
) AS v(code, description)
WHERE NOT EXISTS (
    SELECT 1
    FROM iam.permission p
    WHERE p.code = v.code
);

-- -----------------------------------------------------------------------------
-- Backfill system role grants for tenants created before V27
-- Matrix: OWNER/ADMIN/MANAGER = all 5; USER/READ_ONLY = invoice:read
-- -----------------------------------------------------------------------------

INSERT INTO iam.role_permission (role_id, permission_id, assigned_at)
SELECT r.role_id, p.permission_id, NOW()
FROM iam.role r
         CROSS JOIN iam.permission p
WHERE r.system_role = TRUE
  AND r.code = 'OWNER'
  AND p.code IN (
    'invoice:create', 'invoice:read', 'invoice:update', 'invoice:issue', 'invoice:void'
)
  AND NOT EXISTS (
    SELECT 1
    FROM iam.role_permission rp
    WHERE rp.role_id = r.role_id
      AND rp.permission_id = p.permission_id
);

